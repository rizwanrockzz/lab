public class array1d {
    public static void main(String args[]){
        int a[] = {10,20,30,40,50};
        System.out.print("Given Array : ");
        for(int i=0;i<a.length;i++){
            System.out.print(a[i]+" ");
        }
        System.out.println();
    }
}

